    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $customer = Customer::where('phone', $request->phone['formattedNumber'])->first();
        if ($customer) {
            return response()->json([
                'type' => 'success',
                'customer' => $customer,
            ]);
        }
        else {
            $customer = Customer::create([
                'name' => $request['name'],
                'phone' => $request->phone['formattedNumber'],
                'email' => $request['email'],
            ]);
            return response()->json([
                'type' => 'success',
                'customer' => $customer,
            ]);
        }
    }

